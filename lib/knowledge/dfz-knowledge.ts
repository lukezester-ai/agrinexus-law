/** Локално „умно“ търсене (BM25 + синоними), без външни ключове. */
export function searchKnowledge(query: string): KnowledgeDoc[] {
  return internalKnowledgeSearch(query, KNOWLEDGE_BASE, { limit: 14 }).results;
}

/** Кратко резюме от топ резултатите (извличане на текст, без LLM). */
export function summarizeKnowledgeQuery(query: string, docs: KnowledgeDoc[]): string | null {
  return extractKnowledgeSummary(query, docs);
}

export function getKnowledgeContext(query: string): string {
  const relevantDocs = searchKnowledge(query);
  if (relevantDocs.length === 0) return "";
  
  return relevantDocs.map(doc => 
    `=== ${doc.title} ===
Категория: ${doc.category}
Източник: ${doc.source}

${doc.content}
`
  ).join("\n\n");
}
